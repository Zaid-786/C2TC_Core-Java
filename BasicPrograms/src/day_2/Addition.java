package day_2;

// Addition of Two Number with Static Number.
public class Addition
{

	public static int addition(int a,int b)
	{
		int c=a+b;
		return c;	
	}
	
	
	public static void main(String[] args) 
	{
			
			int x=addition(10,20);
			
			System.out.println("addition of two number="+x);
	}

}
