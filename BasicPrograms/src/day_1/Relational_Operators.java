package day_1;

public class Relational_Operators {

	public static void main(String[] args) {

		int a=1,b=2,c=1;
		
		System.out.println("a<b is:"+(a<b));			//a<b is:true	
		System.out.println("a>b is:"+(a>b));			//a>b is:false
		System.out.println("a<=b is:"+(a<=b));			//a<=b is:true
		System.out.println("a>=b is:"+(a>=b));			//a>=b is:false
		System.out.println("a!=b is:"+(a!=b));			//a!=b is:true
		System.out.println("a==c is:"+(a==c));			//a==c is:true
		
	}

}
