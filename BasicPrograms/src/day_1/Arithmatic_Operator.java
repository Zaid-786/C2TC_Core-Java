package day_1;

public class Arithmatic_Operator {

	public static void main(String[] args) {

		int a,b,c,d;
		
			a=6+5;
			b=6-1;
			c=6*5;
			d=6/5;
			
		System.out.println("a="+a);		// 11
		System.out.println("b="+b);		// 5
		System.out.println("c="+c);		// 30
		System.out.println("d="+d);		// 1
		
		
	}

}
