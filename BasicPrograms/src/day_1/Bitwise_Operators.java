package day_1;

public class Bitwise_Operators {

	public static void main(String[] args) {
			
		int a=1,b=2;
		
		System.out.println("bitwise & AND operation=a&b is :"+(a&b));		//first of all we will convert a,b values into binary form then we will perform bitwise & AND operation
																			//output:- 0
		
		System.out.println("bitwise | OR peration=a|b is :"+(a|b));			//first of all we will convert a,b values into binary form then we will perform bitwise | OR operation
																			//output:- 3
		
		System.out.println("bitwise ^ XOR operation=a^b is :"+(a^b));		//first of all we will convert a,b values into binary form then we will perform bitwise ^ XOR operation
																			//output:- 
		
		System.out.println("bitwise ~ NOT operation=~a is :"+(~a));		//first of all we will convert a,b values into binary form then we will perform bitwise ~ NOT operation

	}

}
